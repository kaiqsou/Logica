/*

1 - Elabore um algoritmo que leia 3 números e apresente o resultado da 
soma do primeiro pelo segundo multiplicado pelo terceiro. 

*/

var x = parseInt(prompt("Digite o primeiro número"));
var y = parseInt(prompt("Digite o segundo número"));
var z = parseInt(prompt("Digite o terceiro número"));

var resultado = (x + y) * z;

alert("Resultado: " + resultado);